#!/usr/bin/env node
/**
 * Copies env vars from license-manager-web → web/.env.local
 * Run: node scripts/import-legacy-env.mjs
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const webRoot = path.join(__dirname, "..");
const legacyPaths = [
  path.join(webRoot, "..", "license-manager-web", ".env.local"),
  path.join(webRoot, "..", "license-manager-web", ".env"),
];

function parseEnvFile(content) {
  const out = {};
  for (const line of content.split("\n")) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const eq = trimmed.indexOf("=");
    if (eq === -1) continue;
    const key = trimmed.slice(0, eq).trim();
    let val = trimmed.slice(eq + 1).trim();
    if ((val.startsWith('"') && val.endsWith('"')) || (val.startsWith("'") && val.endsWith("'"))) {
      val = val.slice(1, -1);
    }
    out[key] = val;
  }
  return out;
}

function loadLegacy() {
  for (const p of legacyPaths) {
    if (fs.existsSync(p)) {
      console.log("Reading", p);
      return parseEnvFile(fs.readFileSync(p, "utf8"));
    }
  }
  throw new Error("No license-manager-web .env.local or .env found");
}

const legacy = loadLegacy();

const lines = [
  "# Generated by scripts/import-legacy-env.mjs — do not commit (gitignored)",
  "",
  "# ── Database (Neon / Vercel Postgres) — REQUIRED, replace with your Neon URL ──",
  "# Create free DB: https://neon.tech → copy connection string",
  "POSTGRES_URL=",
  "DATABASE_URL=",
  "",
  "# ── From license-manager-web ──",
  `JWT_SECRET=${legacy.JWT_SECRET ?? "change-me-min-32-characters-long-secret"}`,
  `ADMIN_API_KEY=${legacy.ADMIN_PASSWORD ?? legacy.ADMIN_API_KEY ?? "change-me-admin-key"}`,
  "",
  "# Admin login (email + password at /admin)",
  `ADMIN_EMAIL=${legacy.ADMIN_EMAIL ?? "mwimulegashame@gmail.com"}`,
  `ADMIN_PASSWORD=${legacy.ADMIN_PASSWORD ?? "Rwanda123@"}`,
  "",
  "# License signing — PEM base64 (same as LICENSE_PRIVATE_KEY in old app)",
  `LICENSE_PRIVATE_KEY=${legacy.LICENSE_PRIVATE_KEY ?? ""}`,
  "",
  "# Gmail SMTP (from old app)",
  "SMTP_HOST=smtp.gmail.com",
  "SMTP_PORT=587",
  `SMTP_USER=${legacy.SMTP_USER ?? ""}`,
  `SMTP_PASS=${legacy.SMTP_PASSWORD ?? legacy.SMTP_PASS ?? ""}`,
  `SMTP_FROM=${legacy.SMTP_USER ?? "noreply@mtglory.com"}`,
  "ADMIN_ALERT_EMAIL=support@mtglory.com",
  "",
  "# Downloads",
  `GITHUB_RELEASE_URL=${legacy.NEXT_PUBLIC_DOWNLOAD_URL_WINDOWS ?? "https://github.com/mtgloryco/glorydesk/releases/latest"}`,
  "WINDOWS_INSTALLER_FILE=GloryDesk_Setup_v1.0.1_Windows.exe",
  "",
  "# Optional — API key still works for scripts/automation",
  "",
  "# MongoDB — NOT used by web/ (Postgres only). Keep for reference:",
  `# MONGODB_URI=${legacy.MONGODB_URI ?? ""}`,
  "",
];

const outPath = path.join(webRoot, ".env.local");
fs.writeFileSync(outPath, lines.join("\n"));
console.log("\nWrote", outPath);
console.log("\nNEXT: Add your Neon Postgres URL to POSTGRES_URL in .env.local");
console.log("Then: npm run dev");
